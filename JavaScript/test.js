const assert = require('assert');
const transpose = require('./transpose');

describe('e.g.', () => {
  it('<i>Stairway to Heaven</i>, up 2 semitones', () => {
    assert.equal(transpose(+2,
      [
        'e|-------5-7-----7-|-8-----8-2-----2-|-0---------0-----|-----------------|',
        'B|-----5-----5-----|---5-------3-----|---1---1-----1---|-0-1-1-----------|',
        'G|---5---------5---|-----5-------2---|-----2---------2-|-0-2-2-----------|',
        'D|-7-------6-------|-5-------4-------|-3---------------|-----------------|',
        'A|-----------------|-----------------|-----------------|-2-0-0---0--/8-7-|',
        'E|-----------------|-----------------|-----------------|-----------------|']),
    [
      'e|-------7-9-----9-|-10-----10-4-----4-|-2---------2-----|------------------|',
      'B|-----7-----7-----|----7--------5-----|---3---3-----3---|-2-3-3------------|',
      'G|---7---------7---|------7--------4---|-----4---------4-|-2-4-4------------|',
      'D|-9-------8-------|-7---------6-------|-5---------------|------------------|',
      'A|-----------------|-------------------|-----------------|-4-2-2---2--/10-9-|',
      'E|-----------------|-------------------|-----------------|------------------|']);
  });
});
